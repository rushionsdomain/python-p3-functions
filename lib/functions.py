
def greet_programmer():
    """prints 'Hello, programmer!'"""
    print("Hello, programmer!")

def greet(name):
    """prints 'Hello, {name}!'"""
    print(f"Hello, {name}!")

def greet_with_default(name="programmer"):
    """prints 'Hello, {name}!'"""
    print(f"Hello, {name}!")

def add(a, b):
    """calculates the sum of a and b"""
    return a + b

def halve(x):
    """halves the input value"""
    return x / 2